package String;

public class StringImmutable {

	public static void main(String[] args) {
		
	String s="Selenium";
		
	System.out.println(s+"automation");
	
	System.out.println(s);
	
	s="Cypress";
	
	System.out.println(s);
	
	String t1 = "Selenium";
	
	System.out.println(t1);
	
	String p = "Abhijit";
	p = p + "Automation Labs";
	System.out.println(p);
	
	String t2 = "Abhijit";
	System.out.println(t2);
	
	

	}

}
