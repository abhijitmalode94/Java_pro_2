package Java_Basics;

public class DataTypes {
	
	public static void main(String[]args) {
		System.out.println("Hello world");
		
		//DataTypes
		//1.primitive datatypes
		
		//1.int
		//size- 4 byte- 32 bits
		//range-  to 
		
		int a=10;
		int b=20;
		System.out.println(a);
		System.out.println(b);
		
		//2.byte
		//size- 1 byte-8 bits
		//range- -127 to 127
		byte b1=20;
		b1=20;
		System.out.println(b1);
		byte b2=30;
		byte b3=40;
		
		//3.short 
		//size- 2byte-16 bites
		// range- -32768 to 32767
		short sh=500;
		short sh1=-100;
		System.out.println(sh+sh1);
		
		//4 long
		//size- 8 byte- 64 bits
		
		long num=9820256368l;
		long num2=552233447898l;
		System.out.println("Value of num is  " +num);
		System.out.println(num2);
			
		//5.float
		//size 4 byte -32 bits
		// range - 7 digit after decimal
		
		float f1=12.33f;
		float f2=33.12f;
		System.out.println(f1+f2);
		float n3=100;
		
		
		//6.char 
		//size 2 byte- 16 bits
		
		char ch='A';
		char ch1='a';
		System.out.println(ch+ch1);
		System.out.println(ch);
		System.out.println(ch1);
		
		System.out.println((int)ch);
		
		//7 boolean
		
		boolean b11=true;
		boolean b22=false;
		System.out.println(b11);
		
		
		
		
		
	}
	

}
